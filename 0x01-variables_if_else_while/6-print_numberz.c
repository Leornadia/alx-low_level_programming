#include <stdio.h>
/**
 * main - Entry point of the program.
 *
 * This program prints all single-digit numbers of base 10
 * starting from 0, followed by a new line.
 *
 * Return: Always returns 0 to indicate successful execution.
 */
int main(void)
{
putchar('0');
putchar('1' + 1);
putchar('1' + 2);
putchar('1' + 3);
putchar('1' + 4);
putchar('1' + 5);
putchar('1' + 6);
putchar('1' + 7);
putchar('1' + 8);
putchar('1' + 9);
putchar('\n');
return (0);
}
